set pagesize 20

COL GROUP_NUMBER FORMAT 99999999999999
COL DISK_NUMBER FORMAT 99999999999999
COL COMPOUND_INDEX FORMAT 99999999999999
COL INCARNATION FORMAT 99999999999999
COL MOUNT_STATUS FORMAT A12
COL HEADER_STATUS FORMAT A13
COL MODE_STATUS FORMAT A11
COL STATE FORMAT A8
COL REDUNDANCY FORMAT A10
COL LIBRARY FORMAT A64
COL TOTAL_MB FORMAT 99999999999999
COL FREE_MB FORMAT 99999999999999
COL NAME FORMAT A30
COL FAILGROUP FORMAT A30
COL LABEL FORMAT A31
COL PATH FORMAT A256
COL UDID FORMAT A64
COL PRODUCT FORMAT A32
COL CREATE_DATE FORMAT A21
COL MOUNT_DATE FORMAT A21
COL REPAIR_TIMER FORMAT 99999999999999
COL READS FORMAT 99999999999999
COL WRITES FORMAT 99999999999999
COL READ_ERRS FORMAT 99999999999999
COL WRITE_ERRS FORMAT 99999999999999
COL READ_TIME FORMAT 99999999999999
COL WRITE_TIME FORMAT 99999999999999
COL BYTES_READ FORMAT 99999999999999
COL BYTES_WRITTEN FORMAT 99999999999999

BREAK ON FAILGROUP SKIP PAGE

SELECT GROUP_NUMBER
     , DISK_NUMBER
     , PATH
     , TOTAL_MB
     , FREE_MB
     , NAME
     , FAILGROUP
     , MODE_STATUS
     , STATE
     , REDUNDANCY
     , MOUNT_STATUS
     , CREATE_DATE
     , MOUNT_DATE
     , COMPOUND_INDEX
     , INCARNATION
     , HEADER_STATUS
     , LIBRARY
     , LABEL
     , UDID
     , PRODUCT
     , REPAIR_TIMER
     , READS
     , WRITES
     , READ_ERRS
     , WRITE_ERRS
     , READ_TIME
     , WRITE_TIME
     , BYTES_READ
     , BYTES_WRITTEN
  FROM V$ASM_DISK
 WHERE GROUP_NUMBER = NVL('&P_GROUP_NUMBER', GROUP_NUMBER)
   AND DISK_NUMBER = NVL('&P_DISK_NUMBER', DISK_NUMBER)
   AND PATH LIKE NVL('&LK_PATH', PATH)
 ORDER BY GROUP_NUMBER, DISK_NUMBER, PATH
/

