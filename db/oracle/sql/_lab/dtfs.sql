@reset

COL TABLESPACE_NAME FORMAT A30
COL TABLESPACE_SIZE FORMAT 99999999999999
COL ALLOCATED_SPACE FORMAT 99999999999999
COL FREE_SPACE FORMAT 99999999999999
COL "%ALLOC" FORMAT 99999
COL "%FREE" FORMAT 999.99

SELECT TABLESPACE_NAME
     , (TABLESPACE_SIZE/1024/1024) TBS_SIZE_MB
     , (ALLOCATED_SPACE/1024/1024) ALLOCATED_MB
     , (FREE_SPACE/1024/1024) FREE_MB
	 , round(ALLOCATED_SPACE/TABLESPACE_SIZE*100,2) "%ALLOC"
	 , round(FREE_SPACE/TABLESPACE_SIZE*100,2) "%FREE"
  FROM DBA_TEMP_FREE_SPACE
/
  
@reset