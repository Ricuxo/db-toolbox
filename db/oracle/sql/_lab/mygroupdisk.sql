set linesize 500

COL GROUP_NUMBER FORMAT 99999999999999
COL NAME FORMAT A30
COL SECTOR_SIZE FORMAT 99999999999999
COL BLOCK_SIZE FORMAT 99999999999999
COL ALLOCATION_UNIT_SIZE FORMAT 99999999999999
COL STATE FORMAT A11
COL TYPE FORMAT A6
COL TOTAL_MB FORMAT 99999999999999
COL FREE_MB FORMAT 99999999999999
COL OFFLINE_DISKS FORMAT 99999999999999

SELECT DG.GROUP_NUMBER
     , DG.NAME
     , DG.SECTOR_SIZE
     , DG.BLOCK_SIZE
     , DG.ALLOCATION_UNIT_SIZE
     , DG.STATE
     , DG.TYPE
     , DG.TOTAL_MB
     , DG.FREE_MB
     , DG.REQUIRED_MIRROR_FREE_MB
     , DG.USABLE_FILE_MB
     , DG.OFFLINE_DISKS
   --, DG.UNBALANCED
     , DG.COMPATIBILITY
     , DG.DATABASE_COMPATIBILITY
  FROM GV$ASM_DISKGROUP DG
     , GV$ASM_DISK D
 WHERE DG.NAME LIKE NVL('&LK_NAME', DG.NAME)
   AND DG.GROUP_NUMBER = NVL('&GROUP_NUMBER', DG.GROUP_NUMBER)
   AND DG.GROUP_NUMBER = D.GROUP_NUMBER
 ORDER BY DG.GROUP_NUMBER, DG.NAME
