set linesize 5000
set pagesize 20

COL JOB FORMAT 99999999999999
COL LOG_USER FORMAT A30
COL PRIV_USER FORMAT A30
COL SCHEMA_USER FORMAT A30
COL LAST_DATE FORMAT A21
COL LAST_SEC FORMAT A8
COL THIS_DATE FORMAT A21
COL THIS_SEC FORMAT A8
COL NEXT_DATE FORMAT A21
COL NEXT_SEC FORMAT A8
COL TOTAL_TIME FORMAT 99999999999999
COL BROKEN FORMAT A6
COL INTERVAL FORMAT A200
COL FAILURES FORMAT 99999999999999
COL WHAT FORMAT A500
COL NLS_ENV FORMAT A500
COL MISC_ENV FORMAT A20
COL INSTANCE FORMAT 99999999999999

SELECT JOB
     , LOG_USER
     , PRIV_USER
     , SCHEMA_USER
     , LAST_DATE
     , LAST_SEC
     , THIS_DATE
     , THIS_SEC
     , NEXT_DATE
     , NEXT_SEC
     , TOTAL_TIME
     , BROKEN
     , INTERVAL
     , FAILURES
     , SUBSTR(WHAT, 1, 500) WHAT
     , SUBSTR(NLS_ENV, 1, 500) NLS_ENV
     , MISC_ENV
     , INSTANCE
  FROM DBA_JOBS
 ORDER BY NEXT_DATE
