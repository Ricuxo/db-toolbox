set lin 1000
col "SPACE_LIMIT (MB)" for 9999,999,999
col "SPACE_USED (MB)" for 9999,999,999 
col "SPACE_RECLAIMABLE (MB)" for 9999,999,999

select name,SPACE_LIMIT/1024/1024/1024 as "SPACE_LIMIT (GB)",
            SPACE_USED/1024/1024 as "SPACE_USED (MB)",
            SPACE_RECLAIMABLE/1024/1024 as "SPACE_RECLAIMABLE (MB)",
            NUMBER_OF_FILES 
from V$RECOVERY_FILE_DEST
/
