set linesize 5000

COL FILE_NAME FORMAT A513
COL FILE_ID FORMAT 99999999999999
COL TABLESPACE_NAME FORMAT A30
COL SIZE_MB FORMAT 99999999999999
COL BLOCKS FORMAT 99999999999999
COL STATUS FORMAT A7
COL RELATIVE_FNO FORMAT 99999999999999
COL AUTOEXTENSIBLE FORMAT A14
COL MAXSIZE_MB FORMAT 99999999999999
COL MAXBLOCKS FORMAT 99999999999999
COL INCREMENT_BY FORMAT 99999999999999
COL USER_MB FORMAT 99999999999999
COL USER_BLOCKS FORMAT 99999999999999

SELECT FILE_NAME
     , FILE_ID
     , TABLESPACE_NAME
     , (BYTES/1024/1024) SIZE_MB
     , BLOCKS
     , STATUS
     , RELATIVE_FNO
     , AUTOEXTENSIBLE
     , (MAXBYTES/1024/1024) MAXSIZE_MB
     , MAXBLOCKS
     , INCREMENT_BY
     , (USER_BYTES/1024/1024) USER_MB
     , USER_BLOCKS
  FROM DBA_TEMP_FILES DTF
