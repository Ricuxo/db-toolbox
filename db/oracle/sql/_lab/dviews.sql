set linesize 5000
set pagesize 9999
set long 99999

COL OWNER FORMAT A30
COL VIEW_NAME FORMAT A30
COL TEXT_LENGTH FORMAT 99999999999999
COL TEXT FORMAT L9999
COL TYPE_TEXT_LENGTH FORMAT 99999999999999
COL TYPE_TEXT FORMAT A4000
COL OID_TEXT_LENGTH FORMAT 99999999999999
COL OID_TEXT FORMAT A4000
COL VIEW_TYPE_OWNER FORMAT A30
COL VIEW_TYPE FORMAT A30
COL SUPERVIEW_NAME FORMAT A30
COL EDITIONING_VIEW FORMAT A15
COL READ_ONLY FORMAT A9

SELECT OWNER
     , VIEW_NAME
     , TEXT_LENGTH
     , TEXT
     , TYPE_TEXT_LENGTH
     , TYPE_TEXT
     , OID_TEXT_LENGTH
     , OID_TEXT
     , VIEW_TYPE_OWNER
     , VIEW_TYPE
     , SUPERVIEW_NAME
     , EDITIONING_VIEW
     , READ_ONLY
  FROM DBA_VIEWS
 WHERE OWNER = UPPER(NVL('&OWNER', OWNER))
   AND VIEW_NAME = UPPER(NVL('&VIEW_NAME', VIEW_NAME))
