@reset
@ln 5000

COL FILE_NAME FORMAT A513
COL FILE_ID FORMAT 99999999999999
COL TABLESPACE_NAME FORMAT A30
COL BYTES FORMAT 99999999999999
COL BLOCKS FORMAT 99999999999999
COL STATUS FORMAT A9
COL RELATIVE_FNO FORMAT 99999999999999
COL AUTOEXTENSIBLE FORMAT A14
COL MAXBYTES FORMAT 99999999999999
COL MAXBLOCKS FORMAT 99999999999999
COL INCREMENT_BY FORMAT 99999999999999
COL USER_BYTES FORMAT 99999999999999
COL USER_BLOCKS FORMAT 99999999999999
COL ONLINE_STATUS FORMAT A13

SELECT FILE_NAME
     , FILE_ID
     , TABLESPACE_NAME
     , BYTES
	 , BYTES/1024/1024 SIZE_MB
     , BLOCKS
     , STATUS
     , RELATIVE_FNO
     , AUTOEXTENSIBLE
     , MAXBYTES
     , MAXBLOCKS
     , INCREMENT_BY
     , USER_BYTES
     , USER_BLOCKS
     , ONLINE_STATUS
  FROM DBA_DATA_FILES DBF
 WHERE DBF.FILE_NAME LIKE NVL('&LK_FILE_NAME',FILE_NAME)
   AND DBF.TABLESPACE_NAME LIKE NVL('&LK_TABLESPACE_NAME',TABLESPACE_NAME)
/
