     , SAMPLE_SIZE
     , LAST_ANALYZED
     , BUFFER_POOL
     , GLOBAL_STATS
     , USER_STATS
  FROM DBA_TAB_PARTITIONS
 WHERE TABLE_OWNER = UPPER(NVL('&TABLE_OWNER', TABLE_OWNER))
   AND TABLE_NAME = UPPER(NVL('&TABLE_NAME', TABLE_NAME))
   AND PARTITION_NAME LIKE UPPER(NVL('&LK_PARTITION_NAME', PARTITION_NAME))
