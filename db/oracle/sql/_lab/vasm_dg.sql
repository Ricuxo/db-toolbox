@reset

set linesize 500
set pagesize 20

COL GROUP_NUMBER FORMAT 99999999999999
COL NAME FORMAT A30
COL SECTOR_SIZE FORMAT 99999999999999
COL BLOCK_SIZE FORMAT 99999999999999
COL ALLOCATION_UNIT_SIZE FORMAT 99999999999999
COL STATE FORMAT A11
COL TYPE FORMAT A6
COL TOTAL_MB FORMAT 99999999999999
COL FREE_MB FORMAT 99999999999999
COL REQUIRED_MIRROR_FREE_MB FORMAT 99999999999999
COL USABLE_FILE_MB FORMAT 99999999999999
COL OFFLINE_DISKS FORMAT 99999999999999
COL UNBALANCED FORMAT A10
COL COMPATIBILITY FORMAT A60
COL DATABASE_COMPATIBILITY FORMAT A60

SELECT GROUP_NUMBER
     , NAME
     , SECTOR_SIZE
     , BLOCK_SIZE
     , ALLOCATION_UNIT_SIZE
     , STATE
     , TYPE
     , TOTAL_MB
     , FREE_MB
     , REQUIRED_MIRROR_FREE_MB
     , USABLE_FILE_MB
     , OFFLINE_DISKS
--   , UNBALANCED
     , COMPATIBILITY
     , DATABASE_COMPATIBILITY
  FROM V$ASM_DISKGROUP
 WHERE NAME LIKE NVL('&LK_NAME', NAME)
   AND GROUP_NUMBER = NVL('&GROUP_NUMBER', GROUP_NUMBER)
 ORDER BY GROUP_NUMBER, NAME
/
